package me.austinatchley;

public class Star {
    public float x;
    public float y;
    public float z;

    public float r;
    public float g;
    public float b;

    public Star(float x, float y, float z, float r, float g, float b){
        this.x = x;
        this.y = y;
        this.z = z;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
